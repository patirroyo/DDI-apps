package examen.model;

public class DragonBall {
	public int number;
	public boolean owned;

	public DragonBall() {
	}

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}

	public boolean isOwned() {
		return owned;
	}

	public void setOwned(boolean owned) {
		this.owned = owned;
	}



}
